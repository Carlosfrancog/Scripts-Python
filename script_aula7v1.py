n1 = int(input('Um valor:'))
n2 = int(input('Outro valor:'))
s = n1 + n2
m = n1 * n2
su = n1 - n2
e = n1 ** n2
di = n1 // n2
rd = n1 % n2
print(f'A soma desses dois números é: {s}')
print(f'A multiplicação desses dois números é: {m}')
print(f'A subtração desses dois números é: {su}')
print(f'A elevacão entre esses dois números é: {e}')
print(f'A divisão inteira entre esses dois números é: {di}')
print(f'O resto da divisão entre esses dois números é: {rd}')
